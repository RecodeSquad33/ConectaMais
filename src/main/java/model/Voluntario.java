package model;

public class Voluntario {

	private String cpf_voluntario;
	private String nome_voluntario;
	private String telefone_voluntario;
	private String email_voluntario;
	private String logradouro_voluntario;
	private String cidade_voluntario;
	private String estado_voluntario;
	private String cep_voluntario;
	private String formacao;
	private String sobre_voluntario;

	public String getCpf_voluntario() {
		return cpf_voluntario;
	}

	public void setCpf_voluntario(String cpf_voluntario) {
		this.cpf_voluntario = cpf_voluntario;
	}

	public String getNome_voluntario() {
		return nome_voluntario;
	}

	public void setNome_voluntario(String nome_voluntario) {
		this.nome_voluntario = nome_voluntario;
	}

	public String getTelefone_voluntario() {
		return telefone_voluntario;
	}

	public void setTelefone_voluntario(String telefone_voluntario) {
		this.telefone_voluntario = telefone_voluntario;
	}

	public String getEmail_voluntario() {
		return email_voluntario;
	}

	public void setEmail_voluntario(String email_voluntario) {
		this.email_voluntario = email_voluntario;
	}

	public String getLogradouro_voluntario() {
		return logradouro_voluntario;
	}

	public void setLogradouro_voluntario(String logradouro_voluntario) {
		this.logradouro_voluntario = logradouro_voluntario;
	}

	public String getCidade_voluntario() {
		return cidade_voluntario;
	}

	public void setCidade_voluntario(String cidade_voluntario) {
		this.cidade_voluntario = cidade_voluntario;
	}

	public String getEstado_voluntario() {
		return estado_voluntario;
	}

	public void setEstado_voluntario(String estado_voluntario) {
		this.estado_voluntario = estado_voluntario;
	}

	public String getCep_voluntario() {
		return cep_voluntario;
	}

	public void setCep_voluntario(String cep_voluntario) {
		this.cep_voluntario = cep_voluntario;
	}

	public String getFormacao() {
		return formacao;
	}

	public void setFormacao(String formacao) {
		this.formacao = formacao;
	}

	public String getSobre_voluntario() {
		return sobre_voluntario;
	}

	public void setSobre_voluntario(String sobre_voluntario) {
		this.sobre_voluntario = sobre_voluntario;
	}

}
